    private void verificaCaps() {

        boolean capsLigado = Toolkit.getDefaultToolkit().getLockingKeyState(
                KeyEvent.VK_CAPS_LOCK);
        if (capsLigado) {
            System.out.println("Caps Lock Ligado!");
        } else {
            System.out.println("Caps Lock Desligado!");
        }

    }

