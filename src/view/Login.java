    private void jButtonOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonOKActionPerformed

        String usuario = jTextFieldUsuario.getText();
        char[] senha = jPasswordFieldSenha.getPassword();

        if (autenticacao.autenticar(usuario, String.valueOf(senha))) {

            JOptionPane.showMessageDialog(this, usuario + " Autenticado", "Autenticação",
                    JOptionPane.INFORMATION_MESSAGE);
            limparCaixas();

            Principal frmPrincipal = new Principal(usuario, "1");
            frmPrincipal.setUsuarioLogado(usuario);
            frmPrincipal.setNivelAcesso("1");
            frmPrincipal.setVisible(true);

            dispose();

        } else if (chance < 2) {

            chance = chance + 1;

            JOptionPane.showMessageDialog(this, "Acesso Negado, tentativa "
                    + chance + " de 3", "Autenticação",
                    JOptionPane.ERROR_MESSAGE);

            limparCaixas();
            jTextFieldUsuario.grabFocus();

        } else {

            JOptionPane.showMessageDialog(this, "Acesso Negado",
                    "Autenticação", JOptionPane.ERROR_MESSAGE);
            System.exit(0);
        }
    }//GEN-LAST:event_jButtonOKActionPerformed

