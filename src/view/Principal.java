    private void verificaNivel(String niveString) {

        switch (niveString) {

            case "1":

                jButtonUsuario.setEnabled(true);
                jButtonProduto.setEnabled(true);
                jButtonEstoque.setEnabled(true);
                subMnuBackup.setEnabled(true);
                subMnuConfiguracao.setEnabled(true);
                break;
            case "2":

                jButtonProduto.setEnabled(true);
                jButtonEstoque.setEnabled(true);
                break;

            default:

                JOptionPane.showMessageDialog(this, "N칤vel de Acesso n칚o reconhecido",
                        "N칤vel Acesso", JOptionPane.ERROR_MESSAGE);
                System.exit(0);
                break;

        }

    }

